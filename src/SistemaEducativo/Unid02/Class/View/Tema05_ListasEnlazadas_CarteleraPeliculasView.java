    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String titulo = JOptionPane.showInputDialog(this, "Título de la película:");
        if (titulo == null || titulo.trim().isEmpty()) {
            return;
        }
        String genero = JOptionPane.showInputDialog(this, "Género:");
        if (genero == null || genero.trim().isEmpty()) {
            return;
        }

        String duracionStr = JOptionPane.showInputDialog(this, "Duración (minutos):");
        int duracion;
        try {
            duracion = Integer.parseInt(duracionStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "¡La duración debe ser un número!");
            return;
        }

        String horarios = JOptionPane.showInputDialog(this, "Horarios (separados por -):");
        if (horarios == null || horarios.trim().isEmpty()) {
            return;
        }
        String[] horariosArray = horarios.split(",");

        int id = cartelera.obtenerUltimoId() + 1;

        cartelera.agregarPelicula(id, titulo, genero, duracion, horariosArray);

        actualizarTabla();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int filaSeleccionada = tbPelicula.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una película primero.");
            return;
        }

        int id = (int) modeloTabla.getValueAt(filaSeleccionada, 0);

        cartelera.eliminarPelicula(id);

        actualizarTabla();
    }//GEN-LAST:event_btnEliminarActionPerformed

