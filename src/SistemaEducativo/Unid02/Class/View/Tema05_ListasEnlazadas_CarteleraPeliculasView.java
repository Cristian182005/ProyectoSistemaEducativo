    private void actualizarTabla() {
        modeloTabla.setRowCount(0); // Limpiar la tabla tbPelicula
        Tema05p1_ListasEnlazadas_NodoPelicula peliculaActual = cartelera.getCabeza();

        while (peliculaActual != null) {
            modeloTabla.addRow(new Object[]{
                peliculaActual.getId(),
                peliculaActual.getTitulo(),
                peliculaActual.getGenero(),
                peliculaActual.getDuracion(),
                String.join(", ", peliculaActual.getHorarios())
            });
            peliculaActual = peliculaActual.getSiguiente();
        }
    }
